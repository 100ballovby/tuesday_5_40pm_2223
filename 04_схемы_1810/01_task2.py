"""
Программа получает 2 числа (a, b) от пользователя и проверяет:
1) а == 30? - вывести 1
2) b == 30? - вывести 2
3) a + b == 30? - вывести 3
4) вывести None;
"""
a = int(input('Введите а: '))
b = int(input('Введите b: '))

if a == 30:
    print(1)
elif b == 30:
    print(2)
elif a + b == 30:
    print(3)
else:
    print(None)
